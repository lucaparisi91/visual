#include <iostream>
#include <glad/glad.h>
#include <GLFW/glfw3.h>
#include "toolsGL.h"

int main(int argc,char ** argv)
{
  GLFWwindow * window;
  
  window=initWindow(800,600,"Monte Carlo Visualizer");
  
  while(!glfwWindowShouldClose(window))
    {
      
      
      glfwSwapBuffers(window);
      glfwPollEvents();
      
    }
  
  closeWindow();
}
